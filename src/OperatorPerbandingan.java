public class OperatorPerbandingan {
    public static void main(String[] args) {
        /*
        * Operasi Perbandingan
        * operasi untuk membandingkan dua buah data
        * operasi yang menghasilkan nilai boolean true or false
        * jika benar maka true
        * jika salah make false
        *
        * Operator          Keterangan
        * >                 Lebih Dari
        * <                 Kurang Dari
        * >=                Lebih Dari sama dengan
        * <=                Kurang dari sama dengan
        * ==                Sama dengan
        * !=                Tidak sama dengan
        *  */

        int valOne = 100;
        int valTwo = 100;

        System.out.println(valOne > valTwo); // false
        System.out.println(valOne < valTwo); // false
        System.out.println(valOne >= valTwo); // true
        System.out.println(valOne <= valTwo); // true
        System.out.println(valOne == valTwo); // true
        System.out.println(valOne != valTwo); // false



    }
}
